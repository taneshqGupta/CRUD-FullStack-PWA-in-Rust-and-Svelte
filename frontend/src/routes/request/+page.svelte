<script lang="ts">
	import type { PostType, Category } from "$lib/types";
	import { createPost, getMyProfile } from "$lib/api";
	import { authStore } from "$lib/auth";
	import { goto } from "$app/navigation";
	import { onMount } from "svelte";
	import { CrossSvg } from "$lib/components/icons";
	import { CATEGORIES } from "$lib/types";

	let newPostDescription = "";
	let newPostCategories: Category[] = [];
	const newPostType: PostType = "request";
	let newPinCode = "";
	let userDefaultPinCode = "";
	let loading = false;
	let success = "";
	let error = "";
	let categorySearch = "";

	$: filteredCategories = CATEGORIES.filter((cat) =>
		cat.toLowerCase().includes(categorySearch.toLowerCase()),
	);

	$: if (!$authStore.loading && !$authStore.isAuthenticated) {
		goto("/login");
	}

	onMount(async () => {
		if ($authStore.isAuthenticated) {
			try {
				const userProfile = await getMyProfile();
				userDefaultPinCode = userProfile.pin_code || "";
				newPinCode = userDefaultPinCode;
			} catch (error) {
				console.error("Error loading user profile:", error);
			}
		}
	});

	async function handleCreatePost() {
		if (!newPostDescription.trim()) {
			error = "Please describe what you need help with";
			return;
		}

		if (newPostCategories.length == 0) {
			error = "Please select at least one category";
			return;
		}

		const description = newPostDescription.trim();
		const categories = newPostCategories;
		const pinCode = newPinCode.trim() || userDefaultPinCode;

		try {
			loading = true;
			error = "";

			await createPost(description, categories, newPostType, pinCode);

			success = "Help request posted successfully!";

			newPostDescription = "";
			newPostCategories = [];
			newPinCode = userDefaultPinCode;

			setTimeout(() => goto("/"), 1500);
		} catch (err) {
			error =
				err instanceof Error ? err.message : "Failed to create post";
		} finally {
			loading = false;
		}
	}
</script>

<svelte:head>
	<title>Request Help - SkillSwap</title>
	<meta name="description" content="Request help from your community" />
</svelte:head>

{#if $authStore.isAuthenticated}
	<div
		class="min-h-full overflow-y-auto p-4 flex items-center justify-center"
	>
		<div
			class="card card-border w-full max-w-md bg-base-100 shadow-2xl my-4"
		>
			<div class="card-body">
				<div class="text-center mb-8">
					<h2
						class="card-title text-2xl font-bold text-base-content justify-center"
					>
						Request Help
					</h2>
					<p class="text-base-content/70 text-sm">
						Learn something from people around you.
					</p>
				</div>

				{#if success}
					<div class="alert alert-success">
						<svg
							xmlns="http://www.w3.org/2000/svg"
							class="stroke-current shrink-0 h-6 w-6"
							fill="none"
							viewBox="0 0 24 24"
						>
							<path
								stroke-linecap="round"
								stroke-linejoin="round"
								stroke-width="2"
								d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z"
							/>
						</svg>
						<span>{success}</span>
					</div>
				{/if}

				{#if error}
					<div class="alert alert-error">
						<svg
							xmlns="http://www.w3.org/2000/svg"
							class="stroke-current shrink-0 h-6 w-6"
							fill="none"
							viewBox="0 0 24 24"
						>
							<path
								stroke-linecap="round"
								stroke-linejoin="round"
								stroke-width="2"
								d="M10 14l2-2m0 0l2-2m-2 2l-2-2m2 2l2 2m7-2a9 9 0 11-18 0 9 9 0 0118 0z"
							/>
						</svg>
						<span>{error}</span>
					</div>
				{/if}

				<form class="space-y-6">
					<div class="form-control">
						<fieldset class="fieldset">
							<legend class="fieldset-legend"
								>What do you wish to learn?</legend
							>
							<textarea
								class="input textarea h-24 w-full"
								placeholder="e.g., I wish to learn maths, design websites .."
								bind:value={newPostDescription}
								required
							></textarea>
						</fieldset>
					</div>

					<div class="grid grid-cols-1 md:grid-cols-2 gap-1 w-full">
						<div class="form-control pb-4">
							<div class="label text-xs font-black mb-2">
								<span class="label-text-alt">Categories</span>
								{#if newPostCategories.length > 0}
									<span class="text-xs text-base-content/60"
										>({newPostCategories.length} selected)</span
									>
								{/if}
							</div>
							{#if newPostCategories.length > 0}
								<div
									class="flex flex-wrap gap-2 mb-2 max-h-32 overflow-y-auto rounded"
								>
									{#each newPostCategories as category}
										<div class="join">
											<div
												class="badge badge-lg badge-soft textarea-xs"
											>
												{category}
											</div>
											<button
												type="button"
												class="btn btn-ghost btn-circle btn-xs ml-1 mt-0.5"
												on:click={() => {
													newPostCategories =
														newPostCategories.filter(
															(c) =>
																c !== category,
														);
												}}
											>
												<CrossSvg />
											</button>
										</div>
									{/each}
								</div>
							{/if}
							<div class="dropdown dropdown-bottom w-full">
								<div
									role="button"
									class="btn btn-soft btn-block justify-start"
									tabindex="0"
								>
									Select Categories
								</div>

								<div
									class="dropdown-content bg-base-100 rounded-box z-[1] w-full p-2 shadow max-h-60 overflow-y-auto"
								>
									<div class="form-control mb-2">
										<input
											class="input input-bordered input-sm"
											placeholder="Search categories..."
											bind:value={categorySearch}
										/>
									</div>

									<ul class="menu w-full">
										{#each filteredCategories as category}
											<li>
												<button
													type="button"
													class="text-left text-sm {newPostCategories.includes(
														category,
													)
														? 'active'
														: ''}"
													on:click={() => {
														if (
															newPostCategories.includes(
																category,
															)
														) {
															newPostCategories =
																newPostCategories.filter(
																	(c) =>
																		c !==
																		category,
																);
														} else {
															newPostCategories =
																[
																	...newPostCategories,
																	category,
																];
														}
													}}
												>
													<span
														class="flex items-center gap-2"
													>
														<input
															type="checkbox"
															checked={newPostCategories.includes(
																category,
															)}
															class="checkbox checkbox-xs"
															readonly
														/>
														{category}
													</span>
												</button>
											</li>
										{/each}
										{#if filteredCategories.length === 0}
											<li class="w-full">
												<span
													class="text-xs opacity-50 w-full"
													>No categories found</span
												>
											</li>
										{/if}
									</ul>
								</div>
							</div>
						</div>

						<div class="form-control">
							<div class="label text-xs font-black mb-2">
								<span class="label-text-alt">
									{userDefaultPinCode
										? `Pin Code [Your Default: ${userDefaultPinCode}]`
										: "Pin Code"}
								</span>
							</div>
							<input
								id="help-pincode"
								name="pincode"
								class="input input-bordered w-full"
								type="text"
								placeholder={userDefaultPinCode ||
									"e.g., 110001"}
								bind:value={newPinCode}
								maxlength="10"
								title="Enter your pin code (optional)"
							/>
						</div>
					</div>

					<div class="flex flex-col gap-3 pt-4">
						<button
							type="button"
							class="btn btn-soft btn-block"
							on:click={handleCreatePost}
						>
							{#if loading}
								<span
									class="loading loading-infinity loading-sm"
								></span>
							{:else}
								Request Help
							{/if}
						</button>
						<button
							type="button"
							class="btn btn-soft btn-block"
							on:click={() => goto("/")}>Cancel</button
						>
					</div>
				</form>
			</div>
		</div>
	</div>
{/if}
